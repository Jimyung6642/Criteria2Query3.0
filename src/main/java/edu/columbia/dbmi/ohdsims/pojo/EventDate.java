package edu.columbia.dbmi.ohdsims.pojo;

public class EventDate {
	private Integer startDays;
	private Integer startCoeff;
	private Integer endDays;
	private Integer endCoeff;
	
	public Integer getStartDays() {
		return startDays;
	}
	public void setStartDays(Integer startDays) {
		this.startDays = startDays;
	}
	public Integer getStartCoeff() {
		return startCoeff;
	}
	public void setStartCoeff(Integer startCoeff) {
		this.startCoeff = startCoeff;
	}
	public Integer getEndDays() {
		return endDays;
	}
	public void setEndDays(Integer endDays) {
		this.endDays = endDays;
	}
	public Integer getEndCoeff() {
		return endCoeff;
	}
	public void setEndCoeff(Integer endCoeff) {
		this.endCoeff = endCoeff;
	}
	
	
}
